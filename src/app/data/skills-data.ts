// Helper function to extract all skills from categories
export const extractAllSkills = (categories: SkillCategory[]): any[] => {
  const skills: any[] = [];
  categories.forEach(category => {
    if (category.skills && Array.isArray(category.skills)) {
      skills.push(...category.skills);
    }
  });
  return skills;
};

